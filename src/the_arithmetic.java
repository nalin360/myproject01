/*
* performing arithmetic operator
* */

public class the_arithmetic {
    public static void main(String[] args) {
        int val1 = 2;
        int val2 = 3;
        System.out.println(val1+val2);
        System.out.println(val1-val2);
        System.out.println(val1/val2);
        System.out.println(val1*val2);
        System.out.println(val1%val2);

    }
}
